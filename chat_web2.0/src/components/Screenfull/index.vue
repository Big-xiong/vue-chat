<template>
  <el-tooltip effect="dark" :content="isFullscreen?$t('navbar.exitFullScreen'):$t('navbar.fullScreen')" placement="bottom">
    <!-- <i class="material-icons-round" @click="click">{{ isFullscreen?'fullscreen_exit':'fullscreen' }}</i> -->
    <svg-icon :icon-class="isFullscreen?'fullscreen_exit':'fullscreen'" @click="click" />
  </el-tooltip>
</template>

<script>
import screenfull from 'screenfull'

export default {
  name: 'Screenfull',
  data() {
    return {
      isFullscreen: false
    }
  },
  mounted() {
    this.init()
  },
  beforeDestroy() {
    this.destroy()
  },
  methods: {
    click() {
      if (!screenfull.enabled) {
        this.$message({
          message: 'you browser can not work',
          type: 'warning'
        })
        return false
      }
      screenfull.toggle()
    },
    change() {
      this.isFullscreen = screenfull.isFullscreen
    },
    init() {
      if (screenfull.enabled) {
        screenfull.on('change', this.change)
      }
    },
    destroy() {
      if (screenfull.enabled) {
        screenfull.off('change', this.change)
      }
    }
  }
}
</script>

